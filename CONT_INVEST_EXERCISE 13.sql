--- COUNTRY ---
CREATE TABLE CONT_COUNTRY (
    ID_COUNTRY INT AUTO_INCREMENT PRIMARY KEY,
    COUNTRY_NAME VARCHAR(75) NOT NULL UNIQUE
);

--- PRODUCT (SÓ O RENDA FIXA) ---
CREATE TABLE CONT_PRODUCT (
    ID_PRODUCT INT AUTO_INCREMENT PRIMARY KEY,
    PRODUCT_NAME VARCHAR(100) NOT NULL UNIQUE
);

--- CASH FLOW TYPE (ENTRADA OU SAÍDA DE DINHEIRO) ---
CREATE TABLE CONT_CASH_FLOW_TYPE (
    ID_CASH_FLOW_TYPE INT AUTO_INCREMENT PRIMARY KEY,
    TYPE_NAME VARCHAR(50) NOT NULL UNIQUE
);

--- CLIENT ---
CREATE TABLE CONT_CLIENT (
    ID_CLIENT INT AUTO_INCREMENT PRIMARY KEY,
    FULL_NAME VARCHAR(150) NOT NULL,
    BIRTH_DATE DATE NOT NULL,
    ID_COUNTRY INT NOT NULL,
    FOREIGN KEY (ID_COUNTRY) REFERENCES CONT_COUNTRY(ID_COUNTRY)
);

--- INVESTMENT ---
CREATE TABLE CONT_INVESTMENT (
    ID_INVESTMENT INT AUTO_INCREMENT PRIMARY KEY,
    ID_CLIENT INT NOT NULL,
    ID_PRODUCT INT NOT NULL,
    INVESTED_VALUE DECIMAL(15,2) NOT NULL,
    ENTRY_DATE DATE NOT NULL,
    FOREIGN KEY (ID_CLIENT) REFERENCES CONT_CLIENT(ID_CLIENT),
    FOREIGN KEY (ID_PRODUCT) REFERENCES CONT_PRODUCT(ID_PRODUCT)
);

--- CASH FLOW (FLUXO DE CAIXA) ---
CREATE TABLE CONT_CASH_FLOW (
    ID_CASH_FLOW INT AUTO_INCREMENT PRIMARY KEY,
    ID_CASH_FLOW_TYPE INT NOT NULL,
    FLOW_VALUE DECIMAL(15,2) NOT NULL,
    FLOW_DATE DATE NOT NULL,
    ID_CLIENT INT NULL,
    COMMENTS VARCHAR(255),
    FOREIGN KEY (ID_CASH_FLOW_TYPE) REFERENCES CONT_CASH_FLOW_TYPE(ID_CASH_FLOW_TYPE),
    FOREIGN KEY (ID_CLIENT) REFERENCES CONT_CLIENT(ID_CLIENT)
);

