===== CONSULTAS =====
--- 1. LISTA DE PAISES ---
SELECT DISTINCT CONT_COUNTRY.COUNTRY_NAME
FROM CONT_CLIENT
JOIN CONT_COUNTRY ON CONT_CLIENT.ID_COUNTRY = CONT_COUNTRY.ID_COUNTRY
ORDER BY CONT_COUNTRY.COUNTRY_NAME;

--- 2. PAISES COM MAIOR INVESTIMENTO (USA MAIOR E ITALY MENOR) ---
SELECT CONT_COUNTRY.COUNTRY_NAME, SUM(CONT_INVESTMENT.INVESTED_VALUE)
FROM CONT_INVESTMENT
JOIN CONT_CLIENT ON CONT_INVESTMENT.ID_CLIENT = CONT_CLIENT.ID_CLIENT
JOIN CONT_COUNTRY ON CONT_CLIENT.ID_COUNTRY = CONT_COUNTRY.ID_COUNTRY
GROUP BY CONT_COUNTRY.COUNTRY_NAME
ORDER BY SUM(CONT_INVESTMENT.INVESTED_VALUE) DESC;

--- 3. ENTRADAS, CUSTOS E LUCRO NO MÊS (juntei o ano e o mês e somei tudo que entrou ali. GROUP BY. Depois relacionar usando o Left Join para não perder os meses sem custo. ---
SELECT 
    T1.ANO,
    T1.MES,
    T1.TOTAL_ENTRADA,
    IFNULL(T2.TOTAL_CUSTO, 0),
    T1.TOTAL_ENTRADA - IFNULL(T2.TOTAL_CUSTO, 0)
FROM
(SELECT 
        YEAR(ENTRY_DATE) ANO,
        MONTH(ENTRY_DATE) MES,
        SUM(INVESTED_VALUE) TOTAL_ENTRADA
    FROM CONT_INVESTMENT
    GROUP BY YEAR(ENTRY_DATE), MONTH(ENTRY_DATE)
) T1
LEFT JOIN
(SELECT 
        YEAR(FLOW_DATE) ANO,
        MONTH(FLOW_DATE) MES,
        SUM(FLOW_VALUE) TOTAL_CUSTO
    FROM CONT_CASH_FLOW
    WHERE ID_CASH_FLOW_TYPE = 2
    GROUP BY YEAR(FLOW_DATE), MONTH(FLOW_DATE)
) T2
ON T1.ANO = T2.ANO AND T1.MES = T2.MES
ORDER BY T1.ANO, T1.MES;

--- 4. MÉDIA DIÁRIA - 27503.333333 ---
SELECT 
SUM(INVESTED_VALUE) / DAY(LAST_DAY(CURDATE()))
FROM CONT_INVESTMENT
WHERE MONTH(ENTRY_DATE) = MONTH(CURDATE());

--- 5. CLIENTES COM MAIS INVESTIMENTOS (DO MAIOR PARA O MENOR) ---
SELECT CONT_CLIENT.FULL_NAME, AVG(CONT_INVESTMENT.INVESTED_VALUE)
FROM CONT_INVESTMENT
JOIN CONT_CLIENT ON CONT_INVESTMENT.ID_CLIENT = CONT_CLIENT.ID_CLIENT
GROUP BY CONT_CLIENT.FULL_NAME
ORDER BY AVG(CONT_INVESTMENT.INVESTED_VALUE) DESC;

